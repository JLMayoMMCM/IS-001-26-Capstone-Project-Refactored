// Emit a SQL INSERT for public.rooms from rooms.generated.json.
// Usage: node scripts/gen-seed-rooms.cjs > replication/sql/seed_rooms.sql
const path = require("path");
const fs = require("fs");

const raw = JSON.parse(
  fs.readFileSync(path.join(__dirname, "..", "lib", "data", "rooms.generated.json"), "utf8")
);

function bucket(code) {
  const c = code.toUpperCase();
  if (c.startsWith("GYM") || c.startsWith("P")) return "Gymnasium";
  if (c.startsWith("A")) return "Admin Building";
  if (c.startsWith("R")) return "Education Building";
  if (c.startsWith("V")) return "Virtual";
  return "Other";
}

function roomTypeEnum(t) {
  switch (t) {
    case "Lecture":    return "lecture";
    case "Laboratory": return "lab";
    case "Office":     return "other";
    case "Backstage":  return "other";
    default:           return "other";
  }
}

function floorNum(code) {
  const c = code.toUpperCase();
  if (c.startsWith("V")) return 0;
  if (c.startsWith("GYM")) return 0;
  // A201, R301, P201 → 2nd digit is floor
  const m = c.match(/[A-Z]+(\d)/);
  return m ? Number(m[1]) : 0;
}

function esc(s) {
  return s.replace(/'/g, "''");
}

const rows = raw
  .filter((r) => r.room_no && r.room_no.trim())
  .map((r) => {
    const code = r.room_no.trim();
    const building = bucket(code);
    const type = roomTypeEnum(r.room_type);
    const floor = floorNum(code);
    const cap = r.capacity && Number(r.capacity) > 0 ? Number(r.capacity) : "NULL";
    return `('${esc(code)}', '${esc(building)}', ${floor}, '${type}'::room_type, ${cap}, true)`;
  });

console.log("-- Generated by scripts/gen-seed-rooms.cjs");
console.log("INSERT INTO public.rooms (room_code, building, floor_number, room_type, capacity, is_active) VALUES");
console.log(rows.join(",\n"));
console.log("ON CONFLICT (room_code) DO NOTHING;");
